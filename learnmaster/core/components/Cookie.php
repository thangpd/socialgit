    /**
     * @param string $name
     * @return bool
     */
    public function __get($name)
    {
        if (isset($_COOKIE[$name])) {
            return urldecode($_COOKIE[$name]);
        }
        return false;
    }

    /**
     * @param $name
     * @param $value
     * @param int $exp
     */
    public function set($name, $value, $exp = 86400)
    {
        setcookie($name, $value, time() + $exp, '/');
    }
