		}
	}

	public function save() {
		$postType = $_POST['post_type'];
		$postId   = lema()->wp->wp_write_post();
		$post     = get_post( $postId );
		$this->responseJson( [
			'code'    => 200,
			'message' => 'Update ' . $postType . ' success',
			'data'    => $this->render( 'partials/' . $postType, [ 'post' => $post ], true )
		] );

	}

