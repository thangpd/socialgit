	public function saveCourse( $post_id ) {
		$data_post = isset( $_POST ) ? $_POST : array();
		if ( isset( $data_post['action'] ) && $data_post['action'] == 'editpost' ) {
			$this->saveMetaBox( $post_id, $data_post );
		}
	}

	//save meta post
	public function saveMetaBox( $post_id, $data_post ) {
		$model = CourseModel::getInstance();
		$model->saveMetaData( $post_id, $data_post );

		//save taxonomy level
		if ( isset( $data_post['course_level'] ) && ! empty( $data_post['course_level'] ) ) {
			$modelLevel = \lema\models\CourseLevelModel::getInstance();
			$modelLevel->saveLevel( $post_id, $data_post['course_level'] );
		}

		//save taxonomy language
		if ( isset( $data_post['course_language'] ) && ! empty( $data_post['course_language'] ) ) {
			$modelLanguage = \lema\models\CourseLanguageModel::getInstance();
			$modelLanguage->saveLanguage( $post_id, $data_post['course_language'] );
		}
	}

