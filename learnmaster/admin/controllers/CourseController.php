	/**
	 * Add filters to course list
	 */
	public function addCourseFilter() {
		$type = 'post';
		if ( isset( $_GET['post_type'] ) ) {
			$type = $_GET['post_type'];
		}
		if ( $type == 'course' ) {
			$users = new \WP_User_Query( array(
				'role' => [ 'lema_instructor' ]
			) );
			$users = $users->get_results();
			if ( $users ) {
				$options = [
					'' => __( 'All Instructor', 'lema' )
				];
				foreach ( $users as $user ) {
					$options[ $user->ID ] = $user->display_name;
				}
				echo ( new Form() )->field( 'instructor', [
					'options'  => $options,
					'type'     => 'select',
					'name'     => 'instructor',
					'template' => '{input}',
					'selected' => isset( $_GET['instructor'] ) ? $_GET['instructor'] : ''

				] );
			}
		}
	}

	public function addCourseFilterQuery( $query ) {
		global $pagenow;
		$type = 'post';
		if ( isset( $_GET['post_type'] ) ) {
			$type = $_GET['post_type'];
		}
		if ( $type == 'course' ) {
			if ( is_admin() && $pagenow == 'edit.php' && isset( $_GET['instructor'] ) && $_GET['instructor'] != '' ) {
				$instructor = new Instructor( $_GET['instructor'] );
				$courses    = $instructor->getCourses();
				if ( empty( $courses ) ) {
					$courses[] = - 1;
				}
				$query->query_vars['post__in'] = $courses;
			}
		}

		return $query;
	}

